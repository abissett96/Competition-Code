task autonomous()
{
	motor[launch3] = 85;	//Activates launcher.

	wait1Msec(5000);			//Waits for speed up.
	intake(127);					//Activates conveyor belt.
	wait1Msec(500);				//Waits for ball to launch.
	intake(0);						//Deactivates conveyor belt.

	wait1Msec(3000);
	intake(127);
	wait1Msec(500);
	intake(0);

	wait1Msec(3000);
	intake(127);
	wait1Msec(500);
	intake(0);

	motor[launch3] = 0;		//Deactivates launcher.
}



task usercontrol()
{

	startTask(PIDLaunchControl);
	//startTask(ManualLaunchControl);

	while (true)
	{
		//Reads speed of the launch wheel.
		speedDebug = getMotorVelocity(launch3) * LAUNCH_RATIO;

		fpsDrive(vexRT[Ch3] /*Forward*/ , vexRT[Ch1] /*Turn*/ );
		intake(127 * (vexRT[Btn5U] - vexRT[Btn5D]));
	}
}
